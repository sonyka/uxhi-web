export function Footer({ settings }: FooterProps) {
  const getHref = (item: NavItem) => {
    if (item.linkType === "external") return item.externalUrl || "#";
    return item.internalLink || "/";
  };

  const currentYear = new Date().getFullYear();

  return (
    <footer className="bg-purple-700 text-white py-12 md:py-16">
      <Container>
        <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-4 gap-8 mb-8">
          {/* Brand */}
          <div className="lg:col-span-2">
            <Link href="/" className="inline-block mb-4">
              {settings.logo?.asset ? (
                <SanityImage
                  value={settings.logo}
                  width={140}
                  height={50}
                  className="h-12 w-auto brightness-0 invert"
                />
              ) : (
                <span className="font-display text-2xl">
                  {settings.siteName || "UXHI"}
                </span>
              )}
            </Link>
            {settings.footerText && (
              <p className="text-purple-200 max-w-md">{settings.footerText}</p>
            )}
          </div>

          {/* Navigation */}
          {settings.footerNavigation && settings.footerNavigation.length > 0 && (
            <div>
              <h3 className="font-semibold text-lg mb-4">Links</h3>
              <ul className="space-y-2">
                {settings.footerNavigation.map((item) => (
                  <li key={item._key}>
                    <Link
                      href={getHref(item)}
                      className="text-purple-200 hover:text-white transition-colors"
                    >
                      {item.label}
                    </Link>
                  </li>
                ))}
              </ul>
            </div>
          )}

          {/* Contact & Social */}
          <div>
            <h3 className="font-semibold text-lg mb-4">Connect</h3>
            {settings.contactEmail && (
              <a
                href={`mailto:${settings.contactEmail}`}
                className="text-purple-200 hover:text-white transition-colors block mb-4"
              >
                {settings.contactEmail}
              </a>
            )}
            {settings.socialLinks && settings.socialLinks.length > 0 && (
              <div className="flex gap-4">
                {settings.socialLinks.map((social) => (
                  <a
                    key={social._key}
                    href={social.url}
                    target="_blank"
                    rel="noopener noreferrer"
                    className="text-purple-200 hover:text-white transition-colors"
                    aria-label={social.platform}
                  >
                    {socialIcons[social.platform] || social.platform}
                  </a>
                ))}
              </div>
            )}
          </div>
        </div>

        {/* Copyright */}
        <div className="border-t border-purple-600 pt-8 text-center text-purple-300 text-sm">
          <p>
            &copy; {currentYear} {settings.siteName || "UXHI"}. All rights
            reserved.
          </p>
        </div>
      </Container>
    </footer>
  );
}
